    <h2>O alívio da tensão pré menstrual</h2>
    <strong>Detalhes</strong>
    <p>Publicado em Quarta, 28 Setembro 2011 15:12</p>
    <p>Escrito por Dra. Érica Rades</p>
    <p><img src="imgs/nov2.jpg" border="0" width="144" height="90" style="border: 2px solid #999999; margin: 0px 10px 0px 0px; float: left;" />É muito comum ouvirmos, principalmente nas grandes cidades, onde a vida agitada não nos dá chance para relaxar, mulheres reclamando que vivem nervosas, sem energia. A maioria associa este sintoma somente ao ritmo acelerado de vida e a noites mal dormidas. Porém, estes sintomas podem estar relacionados a várias outras doenças. Se estes sintomas ocorrem ou pioram alguns dias antes da menstruação, podemos ter um quadro de TPM.</p>
 
<p>A TPM pode piorar a qualidade de vida das mulheres, dos parceiros, da família em alguns casos. Por isso, se os sintomas da TPM estão atrapalhando suas atividades do dia a dia e sua qualidade de vida, o melhor a fazer é procurar um médico. Só ele poderá avaliar se este sintoma é mesmo TPM e orientar o tratamento adequado.</p>
<p><strong>Como saber se tenho TPM?</strong></p>
<p>Inicialmente, você pode perceber se fica mais irritada ou chorosa, se sente inchaço, dores de cabeça, vontade por doces, às vezes perde a concentração ou o controle. Você precisa perceber se estes sintomas aparecem antes da menstruação e desaparecem espontanemante quando a menstruação começa. Se assim for, você pode estar diante da TPM e necessita conversar a respeito disto com seu médico.</p>
<p>Em primeiro lugar, é preciso ficar atento de que os sintomas da TPM podem ser confundidos com causas psicológicas, como depressão ou físicas. Para o diagnóstico correto, é preciso investigar a história clínica do paciente e analisar se a queixa não está relacionada a alguma outra doença. O diagnóstico não pode ser feito apenas pelo sintomas descritos, mas pelo exame clínico e laboratoriais necessários.</p>
<p><strong>Tratamento</strong></p>
<p>O tratamento irá depender do tipo de sintoma e do grau com que estes sintomas afetam sua vida. Numa consulta médica, poderá ser feito um questionário específico e a escolha do tratamento poderá envolver suplementos vitamínicos, medicamentos fitoterápicos e medicamentos com ação antidepressiva nos casos mais sérios.</p>
<p><strong>Outras medidas também podem ajudar no combate à TPM:</strong></p>
<ul>
<li>Procurar ter uma rotina de sono: dormir e acordar todos os dias no mesmo horário.</li>
<li>Manter uma boa alimentação: fazer refeições balanceadas e bem distribuídas durante o dia, reservando à noite para refeições mais leves. Procure comer diariamente uma pequena quantidade de amêndoas e 3 vezes por semana o grão de bico, o feijão ou a lentilha.</li>
<li>Mudar o estilo de vida: tentar inserir na rotina atividades que trarão mais prazer, como ir ao cinema, ao teatro, passear.</li>
<li>Se exercitar: atividades físicas, se feitas de forma correta, só trarão bem-estar, energia e disposição para enfrentar as atividades do dia a dia.</li>
</ul>
