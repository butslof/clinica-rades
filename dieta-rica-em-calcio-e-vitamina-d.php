    <p><b>Vitamina</b></p>

    <p>Além do cálcio não podemos nos esquecer da vitamina D, essencial para a fixação do cálcio nos ossos. Ela só é sintetizada na pele com a ajuda da exposição ao sol. Atualmente, demonstrou-se que a população brasileira apresenta altos índices de hipovitaminose D.<br />
    Para garantir essa vitamina, não esqueça de incluir a gema do ovo e peixes no seu cardápio.</p>

    
    <p><b>Os Efeitos do sol</b></p>

    <p>Bastam 15 minutos de banho de sol para que o corpo sintetize a vitamina D na pele. Os raios ultravioletas ativam a vitamina que está no corpo.</p>

        
    <p><b>Garanta uma dose de sol diária:</b></p>
    - Tome de 15 a 20 minutos de sol por dia. Durante este período não use o filtro solar, pois ele bloqueia a ação benéfica da luz sobre os ossos. Esse tempo é o suficiente.<br />
    - O sol de manhã, até as 10h e o da tardinha, depois das 15h, além de mais saudáveis para a pele, também concentram maior quantidade dos raios capazes de ativar a vitamina D.<br />
    - Em suas atividades físicas prefira shorts, bermudas e camisetas de mangas curtas. Camisas de mangas compridas e calças impedirão uma maior exposição solar.<br />

    <p><b>Exercícios</b></p>

    <p>São fundamentais para fortalecer os ossos. A força do músculo sobre o osso causa microtraumas. É como se ocorressem microfraturas no esqueleto. Os exercícios fazem pressão sobre uma célula chamada osteócito, que vai ativar as atividades de remodelação do osso. O exercício de impacto estimula esse processo.</p>

    <p>Os exercícios mais recomendados são aqueles em que o corpo sofre impacto e os músculos suportam cargas leves. O ideal é fazer exercícios com pesinhos, sempre com a orientação adequada, ou escolher esportes que exijam pequenos saltos e corridas.</p>

